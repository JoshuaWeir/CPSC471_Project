public class Credit {
    private int id;
    private int value;

    // Constructor

    public Credit(int id, int value) {
        this.id = id;
        this.value = value;
    }
    // Getters and Setters

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
